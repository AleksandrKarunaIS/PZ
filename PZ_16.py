class Животное:
    def __init__(self, имя, вид):
        self.имя = имя
        self.вид = вид

    def показать_информацию(self):
        print(f"Имя: {self.имя}, Вид: {self.вид}")


# Пример использования класса
if __name__ == "__main__":
    # Создаем экземпляры животных
    кошка = Животное("Мурка", "Кошка")
    собака = Животное("Шарик", "Собака")

    # Выводим информацию о животных
    кошка.показать_информацию()  # Вывод: Имя: Мурка, Вид: Кошка
    собака.показать_информацию()  # Вывод: Имя: Шарик, Вид: Собака