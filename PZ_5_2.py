#Описать функцию SortDec3(A, B, C), меняющую содержимое переменных A, B, C таким образом, чтобы их значения оказались упорядоченными по убыванию (A, B, C — вещественные параметры, являющиеся одновременно входными и выходными). С помощью этой функции упорядочить по убыванию два данных набора из трех чисел: (A1, B1, C1) и (A2, B2, C2).
def SortDec3(A, B, C):
    if A < B:
        A, B = B, A
    if A < C:
        A, C = C, A
    if B < C:
        B, C = C, B
    return A, B, C

# Первый набор чисел
A1, B1, C1 = 3.2, 5.1, 4.8
# Второй набор чисел
A2, B2, C2 = 7.4, 2.3, 6.5

# Сортировка первого набора
A1, B1, C1 = SortDec3(A1, B1, C1)

# Сортировка второго набора
A2, B2, C2 = SortDec3(A2, B2, C2)

# Вывод результатов
print(f"Первый набор: {A1}, {B1}, {C1}")
print(f"Второй набор: {A2}, {B2}, {C2}")